		for x = 1, #map[y] do
			if map[y][x] == 0 then
			elseif map[y][x] == 1 then
				love.graphics.setColor(230, 225, 206)
				love.graphics.rectangle("fill", x * tsize - tsize, y * tsize - tsize, tsize, tsize)
			end
